public class DailyActivity {
    private int imageDaily;
    private String judulDaily;
    private String deskripsiDaily;
    private String waktuDaily;


    public int getImageDaily() {
        return imageDaily;
    }

    public void setImageDaily(int imageDaily) {
        this.imageDaily = imageDaily;
    }

    public String getJudulDaily() {
        return judulDaily;
    }

    public void setJudulDaily(String judulDaily) {
        this.judulDaily = judulDaily;
    }

    public String getDeskripsiDaily() {
        return deskripsiDaily;
    }

    public void setDeskripsiDaily(String deskripsiDaily) {
        this.deskripsiDaily = deskripsiDaily;
    }

    public String getWaktuDaily() {
        return waktuDaily;
    }

    public void setWaktuDaily(String waktuDaily) {
        this.waktuDaily = waktuDaily;
    }


}
